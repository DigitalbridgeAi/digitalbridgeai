<div class="mb-5">
<h2 class="text-primary section-title">Net Zero 2030</h2>
<p class="mt-3 mb-5">We encourage and empower our portfolio companies to lead in the race to net zero.<br />Achieving net zero greenhouse gas emissions by 2030 is a bold commitment and one of our highest ESG priorities. Given our global strategic relationships with large hyperscale companies, mobile network operators and other telecommunications and technology customers with aggressive decarbonization commitments, we believe this is not only a moral issue but also a business imperative. Thus, we have set aggressive goals to reach net zero emissions on an accelerated time frame and by adhering to the Science Based Targets initiative (SBTi), which defines and promotes emissions reductions in line with climate science.</p>
<h2 id="goals" class="text-primary section-title">Making Progress Toward Our Ambitious Goal</h2>
<p class="mt-3 mb-0">We are moving decisively toward our stated goal to reach net zero emissions on an accelerated time frame. The companies in our Digital Equity portfolio made significant progress during 2021 and are maintaining that momentum. As we continue to add new investments to our portfolios, we intend to follow the timeline below to put each acquisition on a path to net zero.</p></div>
</div>
</div>
<div class="row">
<div class="col-md-6">
<div class="h-100 dark-blue-block" data-aos="fade-right" data-aos-delay="200">
<p class="block-title">2021 Accomplishments</p>
<div class="text-md"><ul>
<li>Developed a strategy for our Net Zero 2030 commitment</li>
<li>Identified qualified vendors who can help our portfolio companies achieve net zero</li>
<li>Initiated calculation of GHG footprint at portfolio companies through vendor introductions</li>
<li>Trained portfolio companies on climate change and energy management through presentations by experts from BSR and Climate Impact Partners</li>
</ul></div>
</div>
</div>
<div class="col-md-6">
<div class="h-100 darkest-blue-block left-arrow" data-aos="fade-right" data-aos-delay="400">
<p class="block-title">2022 Goals</p>
<div class="text-md"><ul>
<li>Complete a GHG footprint for DigitalBridge operations</li>
<li>Encourage all portfolio companies to complete a GHG footprint</li>
<li>Continue supporting companies with introductions to vendors for GHG footprints</li>
<li>Submit SBTi target validation</li>
</ul></div>
