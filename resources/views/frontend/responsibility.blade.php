<h2 class="section-title text-primary mb-4">Our Responsible Investment Policy</h2>
<div class="text-md"><p>Our Responsible Investment Policy serves as the cornerstone of our approach to ESG. This policy guides the integration of both macro-level and company-specific environmental, social and governance (ESG) considerations. Development of this policy was informed by relevant third-party standards, industry best practices and global initiatives, including the Principles for Responsible Investment (PRI), Sustainability Accounting Standards Board (SASB) and the UN Sustainable Development Goals (SDGs).</p>
<p>Our Responsible Investment Policy articulates our ESG vision and mission, affirms our adherence to the PRI, and defines the scope of and identifies material ESG issues. It states that our approach to ESG integration extends throughout the investment life cycle and commits us to publicly reporting our ESG performance on an annual basis.</p></div>
</div>
</section><section id="responsibility-priority-esg-issues" class="pt-0">
<div class="container-xl" data-aos="fade">
<h2 class="section-title text-primary mb-4">Priority ESG Issues </h2>
<div class="text-md"><p>The thoughtful consideration of ESG issues provides an important perspective to identify potential risks and opportunities to create value. DigitalBridge aims to strategically direct our attention and resources to those issues where we seek to achieve the greatest impact throughout the investment process. During both due diligence and ongoing asset management, we typically focus on the most relevant ESG issues, which we have prioritized according to two criteria: those that have the greatest effect on our business and those that are the most important to our stakeholders. The result is a targeted set of ESG priorities we consider for the portfolio companies and investments in both our digital equity and in other relevant portfolios:</p>
<ul>
<li>Climate Change: Energy Efficiency, Greenhouse Gas (GHG) Emissions and Physical Climate Risks</li>
<li>Diversity, Equity and Inclusion (DEI) on our Management Teams and our Boards of Directors</li>
<li>Foreign Corrupt Practices Act, Anti-Bribery and Anti-Corruption Programs</li>
<li>Workplace Health and Safety</li>
<li>Privacy and Data Security (and attendant human rights issues)</li>
</ul></div>
</div>
